"""
Что содержит: dataclass с набором промптов и текстовые шаблоны для анализа, OCR-обработки и суммаризации презентаций.
За что отвечает: за централизованное хранение инструкций, которые отправляются в LLM и VLM при обработке слайдов.
Где используется: импортируется в `src.app.services.presentation_service` и `src.app.services.processor`.
"""

from __future__ import annotations

from dataclasses import dataclass


@dataclass(slots=True)
class ProcessingPrompts:
    """Хранит полный набор промптов для обработки презентаций."""

    PROMPT_EXTRACT_DATE_FROM_SLIDES: str
    PROMPT_DENSE_SLIDE_SUMMARY: str
    PROMPT_SEMANTIC_TEXT_SPLITTER: str
    PROMPT_DENSE_REPORT_SUMMARY: str
    PROMPT_VLM_TRANSCRIBE_SLIDE: str
    PROMPT_VLM_DESCRIBE_IMAGES: str
    PROMPT_VLM_TRANSCRIBE_SLIDE_USER: str = "Полностью перенеси информацию со слайда."
    PROMPT_VLM_DESCRIBE_IMAGES_USER: str = "Опиши фон, изображения и визуальные элементы слайда."


PROMPT_EXTRACT_DATE_FROM_SLIDES = """
Ты анализируешь текст всех слайдов одной презентации и определяешь дату отчета или период,
к которому относится материал.

Верни результат в компактном текстовом виде без пояснений вне шаблона:
DATE: <YYYY-MM-DD | YYYY-MM | YYYY | date_not_found>
PERIOD: <квартал / месяц / диапазон / none>
EVIDENCE: <короткая цитата или список через ;>

Правила:
- используй только даты, которые явно есть в тексте;
- если точной даты нет, но есть период, укажи его в PERIOD;
- если в тексте несколько дат, выбери наиболее вероятную дату самого отчета, а не исторические значения;
- не добавляй markdown, вступления и комментарии.

Текст презентации:
[PASTE_ALL_SLIDES_TEXT_HERE]
"""


PROMPT_DENSE_SLIDE_SUMMARY = """
Ты собираешь финальное семантическое описание одного слайда презентации.

Нужно вернуть строго один JSON-объект со следующими полями:
- `summary`: string
- `tags`: list[string]

Правила:
- объедини информацию из PPTX-текста, структурированного LLM-ответа, OCR/VLM-транскрибации и визуального описания;
- не дублируй одно и то же разными формулировками;
- не выдумывай факты, цифры, подписи и выводы, которых нет в источниках;
- если источники немного расходятся, опирайся на то, что подтверждается несколькими источниками;
- в `summary` дай плотное описание смысла слайда на 3-6 предложений: тема, ключевые тезисы, данные/метрики, важные визуальные акценты и общий вывод;
- в `tags` верни от 3 до 8 коротких тегов в lowercase, без `#`, полезных для поиска по векторной базе;
- ответ должен содержать только JSON без markdown и пояснений.

Контекст:
- report_name: {report_name}
- slide_number: {slide_number}

Источники:
PPTX_EXTRACTED_TEXT:
{pptx_extracted_text}

LLM_STRUCTURED_TEXT:
{llm_structured_text}

VLM_TRANSCRIBED_TEXT:
{vlm_transcribed_text}

VLM_VISUAL_DESCRIPTION:
{vlm_visual_description}
"""


PROMPT_SEMANTIC_TEXT_SPLITTER = """
Ты извлекаешь нормализованную структуру текста одного слайда.

Нужно вернуть строго один JSON-объект со следующими полями:
- `title`: string | null
- `key_points`: list[string]
- `tables`: list[string]
- `notes`: string | null

Правила:
- `title` заполняй только если на слайде явно есть заголовок, иначе `null`;
- `key_points` содержат основные тезисы слайда в виде коротких законченных формулировок;
- `tables` заполняй только данными, которые действительно выглядят как таблица; каждую таблицу передавай одной строкой в читаемом виде;
- `notes` используй для подписей, сносок, дисклеймеров, единиц измерения, контекста и деталей, которые не вошли в другие поля;
- не добавляй информацию от себя и не исправляй факты по догадке;
- если текста мало, все равно верни валидный JSON с пустыми массивами и `null`, где нужно;
- ответ должен содержать только JSON без markdown и пояснений.

Контекст:
- report_name: {report_name}
- slide_number: {slide_number}

Текст слайда:
{slide_text}
"""


PROMPT_DENSE_REPORT_SUMMARY = """
Ты делаешь плотное итоговое summary по всей презентации на основе уже обработанных слайдов.

Верни результат в markdown со следующими разделами:
## Общая тема
## Ключевые выводы
## Основные метрики и факты
## Риски и ограничения
## Практическая ценность

Правила:
- используй только факты из материалов презентации;
- не повторяй дословно одно и то же по нескольким слайдам;
- если данных мало, прямо укажи, что вывод ограничен содержимым слайдов;
- summary должно быть пригодно и для чтения человеком, и для последующей загрузки в БД/поиск.

Материалы презентации:
[PASTE_SLIDE_SUMMARIES_OR_FULL_TEXT_HERE]
"""


PROMPT_VLM_TRANSCRIBE_SLIDE = """
Ты OCR-ассистент для презентационных слайдов.

Нужно вернуть строго один JSON-объект со следующим полем:
- `transcribed_text`: string

Правила:
- перепиши весь читаемый текст со слайда: заголовки, подписи, таблицы, оси графиков, легенды, сноски, логотипные подписи, мелкий текст;
- сохраняй язык оригинала и важные числа без перефразирования;
- если часть текста плохо видна, не выдумывай ее, а пропусти или пометь максимально нейтрально;
- допустимо нормализовать переносы строк, но нельзя терять смысловые блоки;
- ответ должен содержать только JSON без markdown и пояснений.
"""


PROMPT_VLM_DESCRIBE_IMAGES = """
Ты описываешь визуальную часть презентационного слайда.

Нужно вернуть строго один JSON-объект со следующими полями:
- `background`: string | null
- `visuals`: list[string]
- `layout`: string | null

Правила:
- `background` описывает фон, подложку, цветовые плашки, фирменный стиль и общий визуальный тон;
- `visuals` содержит список заметных объектов: фотографии, иконки, диаграммы, графики, таблицы, схемы, иллюстрации, логотипы;
- `layout` кратко описывает композицию слайда: где заголовок, где основной блок, есть ли колонки, центральный график, нижние подписи и т.д.;
- не пересказывай текст слайда, если он уже не является частью визуального объекта;
- не делай выводов о бизнес-смысле, только о визуальном устройстве;
- ответ должен содержать только JSON без markdown и пояснений.
"""


def get_processing_prompts() -> ProcessingPrompts:
    """Возвращает объект со всеми промптами, нужными пайплайну обработки."""
    return ProcessingPrompts(
        PROMPT_EXTRACT_DATE_FROM_SLIDES=PROMPT_EXTRACT_DATE_FROM_SLIDES,
        PROMPT_DENSE_SLIDE_SUMMARY=PROMPT_DENSE_SLIDE_SUMMARY,
        PROMPT_SEMANTIC_TEXT_SPLITTER=PROMPT_SEMANTIC_TEXT_SPLITTER,
        PROMPT_DENSE_REPORT_SUMMARY=PROMPT_DENSE_REPORT_SUMMARY,
        PROMPT_VLM_TRANSCRIBE_SLIDE=PROMPT_VLM_TRANSCRIBE_SLIDE,
        PROMPT_VLM_DESCRIBE_IMAGES=PROMPT_VLM_DESCRIBE_IMAGES,
    )
